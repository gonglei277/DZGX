+(BOOL)judgePassWordLegal:(NSString *)pass{
    
      BOOL result = false;
        // 判断长度大于8位后再接着判断是否同时包含数字和字符
        NSString * regex = @"^[A-Za-z0-9]+$";
        NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", regex];
        result = [pred evaluateWithObject:pass];

       return result;
    
}

